// Program for use of for Loop

public class Program_12 {
    public static void main(String[] args) {
        int num = 21, var;
        System.out.println("\nTable of Number "+num+" is -> ");
        for (int i = 1; i < 11; i++) {
            var = num * i;
            System.out.println(num+" * "+i+" = "+var);
        }
    }
}
